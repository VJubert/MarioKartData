public class Solution {
    int pers;
    int kart;
    int roue;
    int aile;

    Solution(int pers, int kart, int roue, int aile) {
        this.pers = pers;
        this.kart = kart;
        this.roue = roue;
        this.aile = aile;
    }

    public int getPers() {
        return pers;
    }

    public int getKart() {
        return kart;
    }

    public int getRoue() {
        return roue;
    }

    public int getAile() {
        return aile;
    }
}
